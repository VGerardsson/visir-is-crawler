function filtervisir() {
  if ($(".visirart").css("display") == "none") {
    $(".visirart").style.display = "block";
  } else $(".visirart").style.display = "none";
}

function filtermbl() {
  if ($(".mblart").css("display") == "none") {
    $(".mblart").style.display = "block";
  } else $(".mblart").style.display = "none";
}
